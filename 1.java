class ArithmeticOperations {
    public static void main(String[] args) {
        int num1;
        int num2;
        int sum;
        int sub;
        float mul;
        float div;
num1 = 20;
num2 = 10;
sum = num1+num2;
sub = num1-num2;
mul = num1*num2;
div = num1/num2;
System.out.println("Addition of "+num1+" and "+num2+" is "+sum+ "\n");
System.out.println("Substraction of "+num1+" and "+num2+" is "+sub+ "\n");
System.out.println("Multiplication of "+num1+" and "+num2+" is "+mul+ "\n");
System.out.println("Division of "+num1+" and "+num2+" is "+div);

    }
}
